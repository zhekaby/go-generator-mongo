package main

import (
	"fmt"
	"os"
	"text/template"
)

type writer struct {
	parser *Parser
}

type writerData struct {
	*Parser
	*collection
}

func NewWriter(p *Parser) *writer {
	return &writer{p}
}

func (w *writer) Write() {
	for _, collection := range w.parser.collections {
		d := &writerData{
			Parser:     w.parser,
			collection: collection,
		}
		d.write()
	}
}

func (d *writerData) write() error {
	f, err := os.Create(fmt.Sprintf("%s/%s_repository.go", d.Dir, d.collection.Name))
	if err != nil {
		return err
	}
	defer f.Close()
	fmt.Fprintf(f, "// Code generated by s4bmongo. DO NOT EDIT.\n")
	fmt.Fprintf(f, "package %s\n", d.PkgName)

	//interfaces
	t := template.Must(template.New("queue").Parse(writerIface))
	t.Execute(f, d)

	return nil
}
